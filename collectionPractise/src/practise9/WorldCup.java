package practise9;

public class WorldCup {
	private int num;
	private String year;
	private String champion;
	public WorldCup(int num, String year, String champion) {
		this.num = num;
		this.year = year;
		this.champion = champion;
	}
	public int getNum() {
		return num;
	}
	public String getYear() {
		return year;
	}
	public String getChampion() {
		return champion;
	}
	
	
	
}
